/**
 * Position class 
 * This class represents a position in the map
 * @author Francisco Cavaco (51105), Marta Correia (51022) and Miguel Tavares (51966)
 *
 */
public class Position {

	private int x,y;
	
	public Position (int x, int y) {
		this.x = x;
		this.y = y;
	}
	
	public int getX(){
		return x;
	}
	
	public int getY() {
		return y;
	}
}
